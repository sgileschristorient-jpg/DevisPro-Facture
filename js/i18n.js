let currentLang = 'fr';

function switchLanguage(lang) {
    if (lang === currentLang) return;
    
    const translateNode = (node) => {
        if (node.nodeType === 3) { // Text node
            const originalText = node.originalText || node.nodeValue;
            if (!node.originalText) {
                node.originalText = originalText;
            }
            
            const normalized = originalText.replace(/\s+/g, ' ').trim();
            if (normalized && translations[normalized] && translations[normalized][lang]) {
                const matchStart = originalText.match(/^\s*/)[0];
                const matchEnd = originalText.match(/\s*$/)[0];
                node.nodeValue = matchStart + translations[normalized][lang] + matchEnd;
            } else if (lang === 'fr' && node.originalText) {
                node.nodeValue = node.originalText;
            }
        } else if (node.nodeType === 1 && node.nodeName !== 'SCRIPT' && node.nodeName !== 'STYLE') { // Element node
            for (let i = 0; i < node.childNodes.length; i++) {
                translateNode(node.childNodes[i]);
            }
        }
    };

    translateNode(document.body);
    currentLang = lang;
    
    const langText = document.getElementById('lang-text');
    if (langText) {
        langText.textContent = lang === 'fr' ? 'FR' : 'EN';
    }
}

// Add language switcher to navbar
document.addEventListener('DOMContentLoaded', () => {
    // Initial save of text nodes
    const saveOriginalTexts = (node) => {
        if (node.nodeType === 3) {
            node.originalText = node.nodeValue;
        } else if (node.nodeType === 1 && node.nodeName !== 'SCRIPT' && node.nodeName !== 'STYLE') {
            for (let i = 0; i < node.childNodes.length; i++) {
                saveOriginalTexts(node.childNodes[i]);
            }
        }
    };
    saveOriginalTexts(document.body);

    const navbarCollapse = document.getElementById('navbarNav');
    if (navbarCollapse) {
        const langToggleBtn = document.createElement('div');
        langToggleBtn.className = 'd-flex ms-3 align-items-center mt-3 mt-lg-0';
        langToggleBtn.innerHTML = `
            <button class="btn btn-outline-light rounded-pill px-3 py-1 d-flex align-items-center" onclick="switchLanguage(currentLang === 'fr' ? 'en' : 'fr')" style="border-color: rgba(255,255,255,0.2);">
                <i class="bi bi-globe me-2"></i> <span id="lang-text" class="fw-bold">FR</span>
            </button>
        `;
        
        const dlContainer = navbarCollapse.querySelector('.d-flex.mt-3.mt-lg-0');
        if (dlContainer) {
            navbarCollapse.insertBefore(langToggleBtn, dlContainer);
        } else {
            navbarCollapse.appendChild(langToggleBtn);
        }
    }
});
